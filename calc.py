#addition
def add(a,b):
    return a+b
#subtraction
def sub(a,b):
    return a-b
#Multiplication
def mul(a,b):
    return a*b
#devision
def divide(a,b):
    if b==0:
        return "Error! Division by zero."
    else:
        return a/b
#square
def square(a):
    return a**2
#Remainder
def remain(a,b):
    return a%b
    